function getpronoun(pronoun) {
  if (pronoun === "yo") {
    return 0;
  } else if (pronoun === "tú") {
    return 1;
  } else if (pronoun === "vosotros") {
    return 4;
  } else if (pronoun === "ustedes") {
    return 5;
  }
  pronoun = pronoun.split(" ");
  if (pronoun.length === 1) {
    return 2;
  }
  if (pronoun.includes("yo")) {
    return 3;
  }
  return 5;
}

